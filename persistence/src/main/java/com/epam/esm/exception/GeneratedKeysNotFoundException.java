package com.epam.esm.exception;

public class GeneratedKeysNotFoundException extends RuntimeException {
    public GeneratedKeysNotFoundException() {
        super();
    }

    public GeneratedKeysNotFoundException(String message) {
        super(message);
    }

    public GeneratedKeysNotFoundException(String message, Throwable cause) {
        super(message, cause);
    }

    public GeneratedKeysNotFoundException(Throwable cause) {
        super(cause);
    }
}